export const getFeaturedArticle = (): Article | undefined =>
  articles.find((a) => a.featured);

export const getArticleBySlug = (slug: string): Article | undefined =>
  articles.find((a) => a.slug === slug);

export const searchArticles = (query: string): Article[] => {
  if (!query.trim()) return [];
  const q = query.toLowerCase();
  return articles.filter(
    (a) =>
      a.title.toLowerCase().includes(q) ||
      a.excerpt.toLowerCase().includes(q) ||
      a.metadata.characters.some((c) => c.toLowerCase().includes(q)) ||
      a.metadata.tags.some((t) => t.toLowerCase().includes(q)) ||
      (a.quote && a.quote.toLowerCase().includes(q)) ||
      (a.metadata.anime && a.metadata.anime.toLowerCase().includes(q)) ||
      (a.metadata.episode?.toString().includes(q))
  );
};
